//---------Panel Button---------------
PanelButton = function(type,alt){

	this.HTML = document.createElement("a");
	this.HTML.id = YAHOO.util.Dom.generateId(this,"panelButton");
	this.id = this.HTML.id;
	this.HTML.className = type;
	this.HTML.alt = type;
	this.type=type;
	this.HTML.style.cursor = "default";

}

